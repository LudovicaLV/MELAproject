package Model;

public class Agent {

	int numLoc;
	String name; 	
   
	public Agent(int numLoc, String name){
		this.numLoc = numLoc;
		this.name = name;		
	}
	
	
	public int[] getArray(int numLoc){
	int[] name = new int[numLoc];
	return name;
	}
	
	
	public int[] initialArray(int numLoc){
		int[] name = new int[numLoc];
		return name;
		}
	
	
	
}




